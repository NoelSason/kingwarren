# ---- LLM fallback for products without Agribalyse data ----
def llm_estimate(product_name, image_url=None):
    prompt = f"""Estimate environmental impact for this food product: "{product_name}"

Return ONLY a JSON object, no markdown, no code fences, no commentary:
{{
  "co2_total_kg": <float, kg CO2-eq per kg product, typical 0.5-50>,
  "ef_total": <float, environmental footprint score 0.0-1.5, includes eutrophication>,
  "has_plastic_packaging": <true/false>,
  "estimated_ecoscore": <int 0-100, environmental friendliness, 100=best>
}}"""
    content = [{"type": "text", "text": prompt}]
    if image_url:
        content.insert(0, {"type": "image",
                           "source": {"type": "url", "url": image_url}})
    msg = client.messages.create(
        model="claude-haiku-4-5",
        max_tokens=300,
        messages=[{"role": "user", "content": content}],
    )
    txt = msg.content[0].text.strip()
    if txt.startswith("```"):
        txt = txt.split("```")[1]
        if txt.startswith("json"): txt = txt[4:]
        txt = txt.strip()
    return json.loads(txt)

# ---- Per-product scoring (Agribalyse → LLM fallback) ----
def score_real(barcode):
    try:
        r = requests.get(f"https://world.openfoodfacts.org/api/v2/product/{barcode}.json",
                         headers=HEADERS, params={"fields": DEEP}, timeout=10)
        d = r.json()
        if d.get("status") != 1: return None
        p = d["product"]
        agb = (p.get("ecoscore_data") or {}).get("agribalyse") or {}
        pkgs = p.get("packagings") or []
        plastic = any("plastic" in (pk.get("material") or "").lower()
                      or "pet" in (pk.get("material") or "").lower() for pk in pkgs)
        llm_eco = None

        if agb.get("co2_total") is not None:
            co2_kg = agb["co2_total"]
            ef     = agb.get("ef_total") or 0.5
            source = "agribalyse"
            eco    = p.get("ecoscore_score")
        else:
            est = llm_estimate(p.get("product_name","unknown"),
                               image_url=p.get("image_front_url"))
            co2_kg  = est["co2_total_kg"]
            ef      = est["ef_total"]
            plastic = plastic or est["has_plastic_packaging"]
            source  = "llm"
            eco     = est.get("estimated_ecoscore")

        co2     = 1 - min(co2_kg/30.0, 1)
        ef_norm = 1 - min(ef/1.0, 1)
        runoff  = ef_norm * (2 - current_stress) / 2
        plastic_term = 0.3 if plastic else 1.0
        score = round(100*(0.30*co2 + 0.30*runoff + 0.20*plastic_term + 0.20*ef_norm))

        return {"name": p.get("product_name","?")[:25], "source": source,
                "co2": round(co2_kg,1), "ef": round(ef,2),
                "plastic": "Y" if plastic else "N",
                "OceanScore": score, "off_eco": eco}
    except Exception as e:
        print(f"  ! {barcode}: {e}")
        return None

