def ocean_score(category, stress_idx=current_stress):
    f = IMPACTS.get(category, IMPACTS["vegetable"])
    co2     = 1 - min(f["co2"]/WORST["co2"], 1)
    runoff  = 1 - min((f["runoff"]*stress_idx)/(WORST["runoff"]*1.5), 1)
    plastic = 1 - f["plastic"]
    water   = 1 - min(f["water"]/WORST["water"], 1)
    return round(100*(0.35*co2 + 0.30*runoff + 0.20*plastic + 0.15*water))

