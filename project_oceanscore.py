from pyzbar.pyzbar import decode
from PIL import Image, ImageEnhance
import numpy as np
import base64

def image_to_barcode(image_path):
    """Extract barcode number(s) from a product photo. Returns list of strings."""
    img = Image.open(image_path)

    # Pass 1: raw image
    results = decode(img)
    if results:
        return [r.data.decode("utf-8") for r in results]

    # Pass 2: grayscale + contrast boost (helps with blurry/dim photos)
    img_gray = img.convert("L")
    img_boost = ImageEnhance.Contrast(img_gray).enhance(2.0)
    results = decode(img_boost)
    if results:
        return [r.data.decode("utf-8") for r in results]

    # Pass 3: try a few rotations (handles tilted phone photos)
    for angle in [90, 180, 270]:
        results = decode(img.rotate(angle, expand=True))
        if results:
            return [r.data.decode("utf-8") for r in results]

    # Pass 4: LLM vision fallback (uses your existing client from Cell 8)
    try:
        with open(image_path, "rb") as f:
            b64 = base64.standard_b64encode(f.read()).decode()
        msg = client.messages.create(
            model="claude-haiku-4-5", max_tokens=50,
            messages=[{"role":"user","content":[
                {"type":"image","source":{"type":"base64",
                    "media_type":"image/jpeg","data":b64}},
                {"type":"text","text":"Read the barcode number on this product. Return ONLY the digits, nothing else. If no barcode is visible, return 'NONE'."}
            ]}]
        )
        bc = msg.content[0].text.strip()
        return [bc] if bc != "NONE" and bc.isdigit() else []
    except Exception:
        return []

def scan_and_score(image_path):
    """End-to-end: photo → barcode → OceanScore (uses score_real from Cell 8)."""
    barcodes = image_to_barcode(image_path)
    if not barcodes:
        return {"error": "no barcode detected — try a clearer photo"}
    bc = barcodes[0]
    print(f"📷 Detected barcode: {bc}")
    result = score_real(bc)
    if result:
        print(f"🌊 {result['name']}: OceanScore {result['OceanScore']} ({result['source']})")
    return result

