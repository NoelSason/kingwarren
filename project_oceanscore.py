# CELL 5 — Evaluation
# ============================================================
from sklearn.metrics import precision_recall_fscore_support
from scipy.stats import spearmanr

# (a) Forecast accuracy: 1-step persistence baseline vs STL+trend
from sklearn.metrics import mean_squared_error, mean_absolute_error, root_mean_squared_error
for c in df.columns:
    s = df[c].dropna()
    stl = STL(s, period=24*30, robust=True).fit()
    pred = (stl.trend + stl.seasonal).iloc[split:]
    true = s.iloc[split:]
    rmse = root_mean_squared_error(true, pred)
    mae  = mean_absolute_error(true, pred)
    print(f"{c:6s}  RMSE={rmse:.3f}  MAE={mae:.3f}")

# (b) Synthetic anomaly injection on test set
rng = np.random.default_rng(0)
X_test = X[split:].copy()
n_test = len(X_test)
inject_idx = rng.choice(n_test, size=n_test//20, replace=False)
X_test[inject_idx] += rng.normal(4, 1, size=(len(inject_idx), X.shape[1]))

scores = -iso.score_samples(X_test)
threshold = np.quantile(-iso.score_samples(X[:split]), 0.9)
pred_anom = scores > threshold
true_anom = np.zeros(n_test, bool); true_anom[inject_idx] = True
p, r, f, _ = precision_recall_fscore_support(true_anom, pred_anom, average="binary")
print(f"\nSynthetic injection — Precision={p:.2f} Recall={r:.2f} F1={f:.2f}")

